						<h3>《明日方舟》繁中版預計於 6 月底正式推出 巴哈獨家公開製作人「海貓絡合物」專訪</h3>
						<div class="post-metas">
							<div class="post-meta">By Admin</div>
							<div class="post-meta">in <a href="#">Games</a></div>
							<div class="post-meta">3 Comments</div>
						</div>
                        <p>由龍成網路代理營運的《明日方舟》於今（26）日宣布，預計將於 6 月底正式推出，而為了讓玩家能更加了解遊戲相關特色，巴哈姆特 GNN 特別獨家專訪到《明日方舟》遊戲製作人「海貓絡合物」，揭露研發團隊的設計理念及開發秘辛。</p>
                        <p>《明日方舟》製作人海貓絡合物專訪</p>
                        
                        <div class="youtube-embed-wrapper" style="position:relative;padding-bottom:56.25%;padding-top:30px;height:0;overflow:hidden"><iframe allowfullscreen frameborder="0" height="360" src="https://www.youtube.com/embed/thUK9hPcqYw" style="position:absolute;top:0;left:0;width:100%;height:100%" width="640" loading="lazy"></iframe></div>
                        <p>問：是什麼契機想到本作要以天災、源石、感染者作為遊戲背景的主題呢？</p>
                        <p>海貓絡合物：其實選定這個主題是出於一種綜合判斷，最主要的原因是因為我自己比較喜歡，感染者這個主題也是因為我的家人親戚之中，有比較多的人是從事醫學相關的行業，加上受到家庭環境的薰陶，所以對醫學這方面相關的元素非常感興趣，因此我希望能將這方面的要素結合遊戲的設計，加入到遊戲中。</p>
                        <p>問：遊戲內大部分的幹員似乎皆以「動物」作為原型，可以談一下在創作幹員時的設計理念嗎？</p>
                        <p>海貓絡合物：我們本意是想創作一個更加自由開放的背景環境，包括像神話生物、人外種族以及其他我們覺得可能會想得到的非常酷或者很有趣的物種，我們都會列出來然後去看一下哪些適合我們的故事環境，再用我們的風格去把這些物種表現出來。</p>
                        <img src="img/blog/tomorrow1.jpg" alt="">
                        <p>問：《明日方舟》在角色立繪的部分擁有非常高的水準，但風格相較於其他二次元遊戲來說似乎較為保守，是否有什麼原因呢？</p>
                        <p>海貓絡合物：我們主要是希望透過豐富的美術風格讓大家對二次元有一個更加開放的理解，其實大家常說的二次元也只是一種藝術上的表現手法，就是它不一定要和某些特定的元素綁定，所以我們在設計的時候也有一套自己的方式去判斷。
 
                        我們希望以更加風格化的形式去表現我們的作品，另外一方面其實我們遊戲中也是有相對來說穿著比較暴露的角色，但我不想讓大家覺得比較暴露，我希望能夠透過角色自身的氣質表現或是個人經歷去博得大家的喜愛，而不是單純的因為他的外貌。</p>
                            <p>問：本作似乎非常著重每位角色的性格刻畫，且他們都有著大量的角色檔案可供玩家逐步解鎖，這個部份有什麼特別的原因嗎？</p>
                            <p>海貓絡合物：我們想讓角色形象更加立體，所以在創作的時候，我們把一個角色當成是真實存在的人去對待，會去思考這個角色在行為上的前因後果，然後刻畫他的人物歷史，也會去想像他會如何看待某件事情。
 
                            我們認為這樣創作出來的角色才是擁有靈魂的，不管是高星還是低星也好，他們各自的性格、氣質和經歷都是非常獨特的，我們很希望讓玩家在每一個角色身上都能找到非常有意思的一面。 </p>
                            <img src="img/blog/tomorrow2.jpg" alt="">
                            <p>問： 為什麼會設計「阿米婭」這樣一個角色作為羅德島的領袖呢？</p>
                            <p>海貓絡合物：其實我們是希望塑造一個年齡非常小，但是又非常堅強的一個反差形象，也為世界帶來比較柔軟的一面。</p>
                            <p>問：遊戲立項初期，有沒有遇到過哪些印象深刻的難題呢？</p>
                            <p>海貓絡合物：最大的難題應該是有太多想要做的東西吧，就是想法很多，但是要做和能不能做得好，都是需要謹慎考慮的，要盡可能保證這些做出去的東西是優秀的。</p>
                            <p>問：《明日方舟》作為一款新型態戰術攻防手機遊戲，平均每一場戰鬥的時間大約是多久呢？</p>
                            <p>海貓絡合物：主線關卡視流程長度來說只需要大約 3～10 分鐘之間，如果比較難的話就需要長一些，而剿滅作戰一般需要在半小時左右。</p>
                            <p>問：相較於傳統的塔防遊戲，我們發現本作還多了可以讓玩家自由經營的特色系統「基建」，可以向我們簡單介紹一下這個系統嗎？</p>
                            <p>海貓絡合物：羅德島的基建是一個非常重要的設施，是由功能各異的房間組成，不同的房間能提供各種不同的作用，比方說能夠產生出重要資源的房間。</p>
                            <img src="img/blog/tomorrow3.jpg" alt="">
                            <img src="img/blog/tomorrow4.jpg" alt="">
                            <p>問：除了一般的塔防關卡之外，後續是否會有其他新的玩法甚至是 PVP 之類的內容呢?</p>
                            <p>海貓絡合物：我們已經在製作後續的玩法內容了，不過基於現在的調查和玩家給我們的意見，目前不會優先考慮 PVP。</p>
                            <p>問：許多玩家們聽到《明日方舟》繁中版將於今年內上線，對此特別期待，訪談最後您有沒有什麼話想和大家說呢？</p>
                            <p>海貓絡合物：作為《明日方舟》的遊戲製作人，真心希望大家能夠喜歡我們的遊戲和其中非常可愛的幹員們。</p>
                            <p>最後，也要感謝大家的期待，大家的期許是我們最大的動力，希望大家能夠享受這款作品。這當中包含著我們對於視覺設計、現代科學還有幻想故事等等的一些創作結晶，希望大家能從中獲得新鮮感與樂趣，也希望大家能夠在未來支持我們。</p>
